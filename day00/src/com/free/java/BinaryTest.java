public class BinaryTest {
    public static void main(String[] args) {
        //二进制以0b开头
        int num1=0b110;
        //十进制
        int num2=110;
        //八进制以0开头
        int num3=016;
        //十六进制以0x开头
        int num4=0x11;
        //打印以十进制展示
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
    }
}
